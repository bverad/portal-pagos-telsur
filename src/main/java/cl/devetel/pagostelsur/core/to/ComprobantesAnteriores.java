package cl.devetel.pagostelsur.core.to;

public class ComprobantesAnteriores {
	private String fechaPago;
	private Integer monto;
	private String idtrx;
	private Long documentosPagados;
	
	public ComprobantesAnteriores(String fechaPago, Integer monto, String idtrx, Long documentosPagados) {
		super();
		this.fechaPago = fechaPago;
		this.monto = monto;
		this.idtrx = idtrx;
		this.documentosPagados = documentosPagados;
	}
	public String getFechaPago() {
		return fechaPago;
	}
	public void setFechaPago(String fechaPago) {
		this.fechaPago = fechaPago;
	}
	public Integer getMonto() {
		return monto;
	}
	public void setMonto(Integer monto) {
		this.monto = monto;
	}
	public String getIdtrx() {
		return idtrx;
	}
	public void setIdtrx(String idtrx) {
		this.idtrx = idtrx;
	}
	public Long getDocumentosPagados() {
		return documentosPagados;
	}
	public void setDocumentosPagados(Long documentosPagados) {
		this.documentosPagados = documentosPagados;
	}
	@Override
	public String toString() {
		return "ComprobantesAnteriores [fechaPago=" + fechaPago + ", monto=" + monto + ", idtrx=" + idtrx
				+ ", documentosPagados=" + documentosPagados + "]";
	}
	
	
}
